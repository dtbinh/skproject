% E1to10N9
function frequency=E1to10N9(M1,M2,M3,M4,M5,M6,M7,M8,M9)
for c=1:202
    M((1:123),c,1)=M1((15:137),c)/sum(M1((15:137),c))*123;
    M((1:123),c,2)=M2((15:137),c)/sum(M2((15:137),c))*123;
    M((1:123),c,3)=M3((15:137),c)/sum(M3((15:137),c))*123;
    M((1:123),c,4)=M4((15:137),c)/sum(M4((15:137),c))*123;
    M((1:123),c,5)=M5((15:137),c)/sum(M5((15:137),c))*123;
    M((1:123),c,6)=M6((15:137),c)/sum(M6((15:137),c))*123;
    M((1:123),c,7)=M7((15:137),c)/sum(M7((15:137),c))*123;
    M((1:123),c,8)=M8((15:137),c)/sum(M8((15:137),c))*123;
    M((1:123),c,9)=M9((15:137),c)/sum(M9((15:137),c))*123;
end;    
for n=1:9
    Mn((1:123),203,n)=M1((15:137),203)/1000;
end;
Mn((1:7),(1:202),:)=ones(7,202,9);
Mn((117:123),(1:202),:)=ones(7,202,9);
for N=1:9
    for C=1:202
        for fp=8:116
            Mn(fp,C,N)=mean(M(((fp-7):(fp+7)),C,N),1);
        end;
    end;
end;
assignin('base','Mn',Mn);
frequency=Mn((8:116),203,1);
end
